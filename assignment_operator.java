import java.util.Scanner;
public class assignment_operator
{
public static void main(String args[])
{
Scanner sc = new Scanner(System.in);
System.out.println("Enter a number:");
int a = sc.nextInt();
a+=5;
System.out.println("the value of a is:"+a);
a-=5;
System.out.println("the value of a is:"+a);
a*=5;
System.out.println("the value of a is:"+a);
a/=5;
System.out.println("the value of a is:"+a);
a%=5;
System.out.println("the value of a is:"+a);

}}